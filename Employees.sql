CREATE TABLE EMPLOYEES (
  JOBTITLE varchar(100) NOT NULL,
  FIRST_NAME varchar(50) NOT NULL,  -- FNAME OF THE EMPLOYEE
  LAST_NAME varchar(50) NOT NULL,  -- LNAME OF THE EMPLOYEE
  AGE varchar(3) NOT NULL,  -- AGE OF EMPLOYEE
